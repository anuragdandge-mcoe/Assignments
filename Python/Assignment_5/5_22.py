# Write a Python program to create frozensets. Write a Python program to find maximum and the minimum value in a set.


# A frozenset is an immutable version of a set in Python.
# Once a frozenset is created, its elements cannot be modified.


frozen_set = frozenset([1,2,3,4,5,6,7,8,9])
print(" Max from Set = " , max(frozen_set))
print(" Min from Set = " , min(frozen_set))

