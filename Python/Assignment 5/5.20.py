# Write a Python program to create a symmetric difference.


#  the symmetric_difference() method to find the symmetric difference of two sets. 
#  This method returns a new set containing elements that are in either of the sets but not in both. 

set_a = {10,20,30,40,50,60}
set_b = {40,50,60,70,80,90}
symmetric_difference_A_B = set_a.symmetric_difference(set_b)

print(symmetric_difference_A_B)

